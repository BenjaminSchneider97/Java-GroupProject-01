public class Teachers {

    private String tfname;
    private String tlname;

    public Teachers(){
        this.tfname = tfname;
        this.tlname = tlname;
    }

    public String getTfname(){
        return this.tfname;
    }

    public void setTfname(String tfname){
        this.tfname = tfname;
    }

    public String getTlname(){
        return this.tlname;
    }

    public void setTlname(String tlname){
        this.tlname = tlname;
    }

}
